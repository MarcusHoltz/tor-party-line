# Human-readable brute-force time estimate for an N-character vanity prefix.
# Ported from show_generation_estimates() in tor-hidden-service-main/1-up-tor-onion-address.sh
_vanity_time_estimate() {
    case $1 in
        1) echo "instant (milliseconds)" ;;
        2) echo "instant to a few seconds" ;;
        3) echo "seconds" ;;
        4) echo "seconds to ~1 minute" ;;
        5) echo "~1–5 minutes" ;;
        6) echo "~30 minutes to a few hours" ;;
        7) echo "~1–4 days (CPU dependent)" ;;
        8) echo "weeks to months" ;;
        *) echo "unknown" ;;
    esac
}

# Docker mode only: brute-force a custom-prefix .onion address with mkp224o,
# let the user pick one, install its keys, and restart Tor onto the new address.
# WARNING: this permanently replaces the current .onion address.
generate_vanity_onion() {
    header "Vanity .onion Address"

    # Guard: feature depends on the bundled generator (absent in older images)
    if ! command -v mkp224o >/dev/null 2>&1; then
        log_err "Vanity generation needs the bundled mkp224o (rebuild the image)."
        return
    fi

    local old_onion
    old_onion=$(get_onion)
    [ -n "$old_onion" ] && echo -e "  ${DIM}Current: ${old_onion}${NC}"
    echo -e "  ${YELLOW}This generates a brand-new .onion address with a custom prefix.${NC}"
    echo -e "  ${YELLOW}${BOLD}Your current address will be destroyed and cannot be recovered.${NC}\n"

    # Prompt + validate the prefix (Tor base32 alphabet: a–z and digits 2–7)
    local prefix
    while true; do
        echo -ne "  ${BOLD}Desired prefix${NC} ${DIM}(1–8 chars, a–z and 2–7):${NC} "
        read -r prefix
        prefix=$(to_lower "$prefix")
        if [ -z "$prefix" ]; then
            log_info "Cancelled"
            return
        fi
        if [[ "$prefix" =~ ^[a-z2-7]{1,8}$ ]]; then
            break
        fi
        log_warn "Invalid prefix. Allowed: lowercase a–z and digits 2–7 (base32), max 8 chars."
    done

    echo -e "\n  ${DIM}Address will begin with:${NC} ${WHITE}${prefix}${NC}"
    echo -e "  ${DIM}Estimated search time:${NC} $(_vanity_time_estimate ${#prefix})"
    if [ ${#prefix} -ge 7 ]; then
        echo -e "  ${YELLOW}A prefix this long can take a very long time — press Ctrl-C to abort.${NC}"
    fi
    if ! confirm_no "\n  ${BOLD}Generate 3 candidates now? [y/N]: ${NC}"; then
        log_info "Cancelled"
        return
    fi

    local vanity_dir
    vanity_dir=$(mktemp -d /tmp/vanity_XXXXXX) || { log_err "Could not create temp dir"; return; }

    # Run the generator in the foreground so the user sees live progress.
    # The global INT trap (cleanup) fires on Ctrl-C; we check the exit status
    # explicitly so an abort returns to the menu instead of installing keys.
    echo -e "\n  ${DIM}Generating — this runs until ${BOLD}3${NC}${DIM} addresses are found...${NC}\n"
    if ! mkp224o -n 3 -d "$vanity_dir" "$prefix"; then
        rm -rf "$vanity_dir"
        log_warn "Vanity generation cancelled."
        return
    fi

    # Collect the generated candidate directories
    local dirs=()
    mapfile -t dirs < <(find "$vanity_dir" -mindepth 1 -maxdepth 1 -type d | sort)
    if [ ${#dirs[@]} -eq 0 ]; then
        rm -rf "$vanity_dir"
        log_err "No addresses were generated. Please try again."
        return
    fi

    echo -e "\n  ${BOLD}${CYAN}Select an address to use:${NC}"
    local i
    for i in "${!dirs[@]}"; do
        local host
        host=$(cat "${dirs[$i]}/hostname" 2>/dev/null || echo "(unreadable)")
        echo -e "    ${BOLD}${WHITE}$((i + 1))${NC} ${CYAN}│${NC} ${host}"
    done

    echo -ne "\n  ${BOLD}Number [default 1]: ${NC}"
    local choice
    read -r choice
    local selected
    if [[ "$choice" =~ ^[0-9]+$ ]] && [ "$choice" -ge 1 ] && [ "$choice" -le ${#dirs[@]} ]; then
        selected="${dirs[$((choice - 1))]}"
    else
        selected="${dirs[0]}"
        log_info "Using address #1"
    fi

    echo -e "\n  ${YELLOW}Installing keys will stop Tor and switch to the new address.${NC}"
    if ! confirm_no "  ${BOLD}Proceed? [y/N]: ${NC}"; then
        rm -rf "$vanity_dir"
        log_info "Cancelled — no changes made."
        return
    fi

    # Install the chosen key material into Tor's hidden-service directory
    mkdir -p "$TOR_DIR/hidden_service"
    cp "$selected/hostname" \
       "$selected/hs_ed25519_secret_key" \
       "$selected/hs_ed25519_public_key" \
       "$TOR_DIR/hidden_service/"
    chmod 600 "$TOR_DIR/hidden_service/hs_ed25519_secret_key" \
              "$TOR_DIR/hidden_service/hs_ed25519_public_key"
    chmod 700 "$TOR_DIR/hidden_service"
    chown -R debian-tor:debian-tor "$TOR_DIR/hidden_service" 2>/dev/null || true
    rm -rf "$vanity_dir"

    # Keys are on disk; read address directly — no in-place Tor restart needed.
    # Tor must be restarted via the normal container/relay cycle to pick up the
    # new hidden-service keys and publish the updated descriptor.
    local new_onion
    new_onion=$(cat "$TOR_DIR/hidden_service/hostname" 2>/dev/null || echo "(unknown)")
    log_ok "New vanity address: ${new_onion}"
    echo ""
    log_warn "Keys installed. Restart the relay to activate this address:"
    echo -e "  ${DIM}  Relay daemon :  docker compose restart${NC}"
    echo -e "  ${DIM}  Relay session:  exit this menu, then re-run the relay${NC}"
    echo ""
}

