# Shared Tor launch: ensure the data dir, pick a free SOCKS port, write the
# torrc, clear the log, and spawn Tor in the background (TOR_PID). Used by both
# the blocking start_tor and the non-blocking start_tor_bg. Returns 1 if the
# data dir is not writable.
_tor_spawn() {
    mkdir -p "$DATA_DIR" "$TOR_DIR" 2>/dev/null || true
    if [ ! -w "$DATA_DIR" ]; then
        log_err "Data directory is not writable: $DATA_DIR"
        log_err "Fix with:  sudo chown -R $(id -un):$(id -gn) \"$DATA_DIR\""
        return 1
    fi
    _bump_socks_port_if_needed   # avoid clashing with a system Tor on 9050
    setup_tor
    > "$TOR_DIR/tor.log"          # fresh log so we only parse this run's output
    tor -f "$TOR_CONF" &>/dev/null &
    TOR_PID=$!
    save_pid "tor" "$TOR_PID"
}

# Wait for Tor (TOR_PID) to reach "Bootstrapped 100%" by tailing its log.
# Single-hop has no SOCKS port (SocksPort 0) so there is nothing to wait for.
# First bootstrap (no cached consensus) gets a longer timeout. Returns 1 if Tor
# dies or the wait times out. Shared by the freshly-spawned and already-running
# paths in start_tor — reaching 100% implies the SOCKS listener is open, so an
# outbound dial won't hit "Connection refused".
_tor_wait_bootstrap() {
    [ "$SINGLE_HOP" -eq 1 ] && return 0
    local tor_log="$TOR_DIR/tor.log" waited=0 timeout=120 last_pct="" bline pct
    [ ! -d "$TOR_DIR/data" ] && [ ! -f "$ONION_FILE" ] && timeout=300
    while [ $waited -lt $timeout ]; do
        if ! kill -0 "$TOR_PID" 2>/dev/null; then
            echo ""; log_err "Tor process died! Check $tor_log"
            if [ ! -f "$TOR_CONF" ]; then
                log_err "torrc missing — likely a write-permission error on $DATA_DIR"
                log_err "Fix with:  sudo chown -R $(id -un):$(id -gn) \"$DATA_DIR\""
            fi
            local _log_tail=""
            [ -f "$tor_log" ] && _log_tail=$(tail -5 "$tor_log" 2>/dev/null)
            [ -n "$_log_tail" ] && echo "$_log_tail"
            # Detect the "another Tor process using the same data directory" error
            if echo "$_log_tail" | grep -q "another Tor process is running with the same data directory"; then
                echo ""
                log_warn "Another Tor process is already using this data directory."
                local _tor_procs
                _tor_procs=$(ps aux 2>/dev/null | grep -E '\s+[0-9:]+ +tor\b' | grep -v grep || true)
                if [ -n "$_tor_procs" ]; then
                    echo -e "  ${BOLD}Conflicting Tor process(es):${NC}"
                    echo "$_tor_procs" | while IFS= read -r _line; do
                        echo -e "    ${DIM}${_line}${NC}"
                    done
                    local _tor_pid
                    _tor_pid=$(echo "$_tor_procs" | awk '{print $2}' | head -1)
                    echo ""
                    echo -e "  ${YELLOW}To kill it, run:${NC}  ${BOLD}kill ${_tor_pid}${NC}"
                    echo -e "  ${DIM}Or to kill all tor processes:  sudo pkill -x tor${NC}"
                else
                    echo -e "  ${YELLOW}Could not find the process — try:  ${BOLD}sudo pkill -x tor${NC}"
                fi
                echo ""
            fi
            return 1
        fi
        bline=$(grep -o "Bootstrapped [0-9]*%.*" "$tor_log" 2>/dev/null | tail -1 || true)
        if [ -n "$bline" ]; then
            pct=$(echo "$bline" | grep -o '[0-9]*%' || true)
            if [ -n "$pct" ] && [ "$pct" != "$last_pct" ]; then
                echo -ne "\r  ${DIM}${bline}${NC}                    "
                last_pct="$pct"
            fi
            [[ "$bline" == *"100%"* ]] && { echo ""; return 0; }
        else
            echo -ne "\r  ${DIM}Waiting for Tor to start...${NC} "
        fi
        sleep 1; waited=$((waited + 1))
    done
    echo ""; log_err "Timed out waiting for Tor to bootstrap (${timeout}s)"
    return 1
}

start_tor() {
    if [ -n "$TOR_PID" ] && kill -0 "$TOR_PID" 2>/dev/null; then
        # Tor was launched in the background at startup; it may still be
        # bootstrapping, so wait before returning to the caller.
        log_info "Tor is already running (PID $TOR_PID)"
    else
        log_info "Starting Tor..."
        if [ ! -d "$TOR_DIR/data" ] && [ ! -f "$ONION_FILE" ]; then
            echo -e "  ${DIM}First bootstrap detected — Tor needs to download network consensus.${NC}"
            echo -e "  ${DIM}This is a one-time process and may take a minute or two.${NC}"
        fi
        [ "$SNOWFLAKE_ENABLED" -eq 1 ] && \
            echo -e "  ${DIM}Snowflake bridge enabled — this may take longer than usual, please be patient.${NC}"
        _tor_spawn || return 1
    fi

    _tor_wait_bootstrap || return 1

    # Wait for onion address file (should appear quickly after 100%)
    local addr_wait=0
    while [ ! -f "$ONION_FILE" ] && [ $addr_wait -lt 15 ]; do
        sleep 1
        addr_wait=$((addr_wait + 1))
    done

    if [ -f "$ONION_FILE" ]; then
        log_ok "Tor hidden service active"
        echo -e "  ${BOLD}${GREEN}Your address: ${WHITE}$(cat "$ONION_FILE")${NC}"
        return 0
    else
        log_err "Tor bootstrapped but hidden service address not found"
        return 1
    fi
}

stop_tor() {
    stop_auto_listener
    if [ -n "$TOR_PID" ] && kill -0 "$TOR_PID" 2>/dev/null; then
        kill "$TOR_PID" 2>/dev/null || true
        wait "$TOR_PID" 2>/dev/null || true
        TOR_PID=""
        log_ok "Tor stopped"
    else
        log_info "Tor is not running"
    fi
}

# Non-blocking Tor start: launches Tor in background and returns immediately.
# Used at startup so the menu appears while Tor bootstraps.
# Silent no-op if tor is not installed (menu will guide the user to option 9).
start_tor_bg() {
    [ -n "$TOR_PID" ] && kill -0 "$TOR_PID" 2>/dev/null && return 0
    check_dep tor || return 0
    _tor_spawn
}

