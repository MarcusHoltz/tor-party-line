#=============================================================================
# VOLUME-DOWN DOUBLE-TAP PTT MONITOR (Termux only, experimental)
#=============================================================================

start_vol_monitor() {
    local trigger_file="${1:-$DATA_DIR/run/vol_ptt_trigger_$$}"
    [ "$VOL_PTT" -ne 1 ] && return
    [ "$IS_TERMUX" -ne 1 ] && return
    if ! check_dep jq; then
        log_warn "jq not found — Volume PTT disabled"
        return
    fi
    if ! check_dep termux-volume; then
        log_warn "termux-volume not found — Volume PTT disabled"
        return
    fi

    overwrite_rm "$trigger_file"

    (
        local last_vol=""
        local last_tap=0
        local restore_vol=""

        while [ -f "$CONNECTED_FLAG" ]; do
            local cur_vol
            cur_vol=$(termux-volume 2>/dev/null \
                | jq -r '.[] | select(.stream=="music") | .volume' 2>/dev/null \
                || echo "")

            # Remember the initial volume so we can restore after detection
            if [ -n "$cur_vol" ] && [ -z "$restore_vol" ]; then
                restore_vol="$cur_vol"
            fi

            if [ -n "$cur_vol" ] && [ -n "$last_vol" ]; then
                local drop=$(( last_vol - cur_vol ))

                if [ "$drop" -ge 2 ] 2>/dev/null; then
                    # Rapid double-tap: both presses landed in one poll cycle
                    touch "$trigger_file"
                    last_tap=0
                    # Restore volume for next use
                    if [ -n "$restore_vol" ]; then
                        termux-volume music "$restore_vol" 2>/dev/null || true
                        last_vol="$restore_vol"
                        sleep 0.5
                        continue
                    fi
                elif [ "$drop" -ge 1 ] 2>/dev/null; then
                    # Single press — check if second press follows within 1s
                    local now; now=$(date +%s)
                    if [ "$last_tap" -gt 0 ] && [ $((now - last_tap)) -le 1 ]; then
                        touch "$trigger_file"
                        last_tap=0
                        if [ -n "$restore_vol" ]; then
                            termux-volume music "$restore_vol" 2>/dev/null || true
                            last_vol="$restore_vol"
                            sleep 0.5
                            continue
                        fi
                    else
                        last_tap=$now
                    fi
                fi
            fi
            last_vol="$cur_vol"
            sleep 0.4
        done
    ) &
    VOL_MON_PID=$!
}

# Check if an incoming call arrived on the background listener
check_auto_listen() {
    if [ -f "$AUTO_LISTEN_FLAG" ]; then
        overwrite_rm "$AUTO_LISTEN_FLAG"
        touch "$CONNECTED_FLAG"
        echo -e "\n  ${GREEN}${BOLD}Incoming call detected!${NC}" >&2
        sleep 0.5
        in_call_session "$RECV_PIPE" "$SEND_PIPE" ""
        cleanup_call
        # Restart listener for next call
        start_auto_listener
        return 0
    fi
    return 1
}

# Start listening for incoming calls (manual, blocking)
listen_for_call() {
    if [ -z "$SHARED_SECRET" ]; then
        log_err "No shared secret set! Use option 1 first."
        return 1
    fi

    if [ $DOCKER_MODE -eq 1 ]; then
        wait_for_tor
    else
        start_tor || return 1
    fi

    # Stop auto-listener if running (we'll do manual listen)
    stop_auto_listener

    local onion
    onion=$(get_onion)
    header "Listening for Calls"
    echo -e "  ${GREEN}Your address:${NC} ${BOLD}${WHITE}$onion${NC}"
    echo -e "  ${GREEN}Listening on:${NC} port $LISTEN_PORT"
    echo -e "\n  ${DIM}Share your .onion address with the caller.${NC}"
    echo -e "  ${DIM}[Q] Stop listening  [B] Listen in background${NC}\n"

    mkdir -p "$AUDIO_DIR"
    log_info "Waiting for incoming connection..."

    overwrite_rm "$RECV_PIPE" "$SEND_PIPE"
    mkfifo "$RECV_PIPE" "$SEND_PIPE"

    local incoming_flag="$DATA_DIR/run/incoming_$$"
    overwrite_rm "$incoming_flag"

    socat "TCP-LISTEN:$LISTEN_PORT,reuseaddr" \
        "SYSTEM:touch $incoming_flag; cat $SEND_PIPE & cat > $RECV_PIPE" \
        2>/dev/null &
    local socat_pid=$!
    save_pid "socat" "$socat_pid"

    while [ ! -f "$incoming_flag" ]; do
        if ! kill -0 "$socat_pid" 2>/dev/null; then
            log_err "Listener stopped unexpectedly"
            overwrite_rm "$RECV_PIPE" "$SEND_PIPE" "$incoming_flag"
            # Restart auto-listener if enabled
            start_auto_listener
            return 1
        fi
        # Read user input with 1-second timeout
        local user_input=""
        if read -r -t 1 user_input 2>/dev/null; then
            case "$user_input" in
                q|Q)
                    # Stop all listening (manual + auto), return to menu
                    kill "$socat_pid" 2>/dev/null || true
                    wait "$socat_pid" 2>/dev/null || true
                    overwrite_rm "$RECV_PIPE" "$SEND_PIPE" "$incoming_flag"
                    stop_auto_listener
                    AUTO_LISTEN=0
                    save_config
                    log_info "Stopped listening."
                    return 0
                    ;;
                b|B)
                    # Move to background: kill manual socat, enable auto-listen
                    kill "$socat_pid" 2>/dev/null || true
                    wait "$socat_pid" 2>/dev/null || true
                    overwrite_rm "$RECV_PIPE" "$SEND_PIPE" "$incoming_flag"
                    AUTO_LISTEN=1
                    save_config
                    start_auto_listener
                    log_ok "Listening in background. Returning to menu."
                    sleep 1
                    return 0
                    ;;
            esac
        fi
    done

    touch "$CONNECTED_FLAG"
    log_ok "Call connected!"
    in_call_session "$RECV_PIPE" "$SEND_PIPE" ""
    cleanup_call

    # Restart auto-listener if enabled
    start_auto_listener
}

# Call a remote .onion address
# Dial $remote_onion over Tor and wait for the circuit to establish.
# Sets DIAL_RESULT to ok|cancelled|timeout|refused. On "ok" socat is left running
# (pid saved as socat_call) with the FIFOs created; the caller must then open the
# pipes and run in_call_session. Reused for both the initial dial and reconnects.
DIAL_RESULT=""
_dial_remote() {
    overwrite_rm "$RECV_PIPE" "$SEND_PIPE"
    mkfifo "$RECV_PIPE" "$SEND_PIPE"

    # The SYSTEM command touches this flag the instant socat's TCP connect succeeds.
    # We wait for the flag before opening FIFOs so exec 3</4> never blocks indefinitely.
    local call_connected_flag="$DATA_DIR/run/call_connected_$$"
    overwrite_rm "$call_connected_flag"

    # Connect via Tor SOCKS proxy using socat.
    socat "SOCKS4A:127.0.0.1:${remote_onion}:${LISTEN_PORT},socksport=${TOR_SOCKS_PORT}" \
          "SYSTEM:touch $call_connected_flag; cat $SEND_PIPE & cat > $RECV_PIPE" \
          2>/dev/null &
    local socat_pid=$!
    save_pid "socat_call" "$socat_pid"

    # Animated connecting indicator
    (
        local dots=""
        while true; do
            for dots in "." ".." "..." "   "; do
                echo -ne "\r  ${CYAN}${BOLD}Connecting${dots}${NC}   " >&2
                sleep 0.3
            done
        done
    ) &
    local spinner_pid=$!

    # Wait for socat to establish the Tor circuit (up to 120 s), with [Q] cancel.
    # Tor hidden-service connections typically take 15–60 s on first circuit build.
    local connect_timeout=120
    local connect_start; connect_start=$(date +%s)
    DIAL_RESULT="timeout"

    while true; do
        # socat exited before connecting (bad address, refused, SOCKS error, etc.)
        if ! kill -0 "$socat_pid" 2>/dev/null; then
            DIAL_RESULT="refused"
            break
        fi
        # SYSTEM command fired → TCP established
        if [ -f "$call_connected_flag" ]; then
            DIAL_RESULT="ok"
            break
        fi
        # Hard timeout
        if [ $(( $(date +%s) - connect_start )) -ge $connect_timeout ]; then
            DIAL_RESULT="timeout"
            break
        fi
        # Non-blocking key check (1-second poll)
        local _key=""
        if read -r -t 1 _key 2>/dev/null; then
            case "$_key" in
                q|Q)
                    DIAL_RESULT="cancelled"
                    kill "$socat_pid" 2>/dev/null || true
                    wait "$socat_pid" 2>/dev/null || true
                    break
                    ;;
            esac
        fi
    done

    kill "$spinner_pid" 2>/dev/null || true
    wait "$spinner_pid" 2>/dev/null || true
    echo -ne "\r                              " >&2
    echo "" >&2
    overwrite_rm "$call_connected_flag"

    if [ "$DIAL_RESULT" != "ok" ]; then
        kill "$socat_pid" 2>/dev/null || true
        wait "$socat_pid" 2>/dev/null || true
    fi
}

call_remote() {
    if [ -z "$SHARED_SECRET" ]; then
        log_err "No shared secret set! Use option 1 first."
        return 1
    fi

    header "Make a Call"
    if [ -z "${remote_onion:-}" ]; then
        echo -ne "  ${BOLD}Enter .onion address: ${NC}"
        read -r remote_onion
    else
        echo -e "  ${DIM}Calling:${NC} ${remote_onion}"
    fi

    if [ -z "$remote_onion" ]; then
        log_warn "No address entered"
        return 1
    fi

    # Strip http(s):// prefix (some QR scanners auto-prepend it)
    remote_onion="${remote_onion#http://}"
    remote_onion="${remote_onion#https://}"

    # Append .onion if not present
    if [[ "$remote_onion" != *.onion ]]; then
        remote_onion="${remote_onion}.onion"
    fi

    if [ $DOCKER_MODE -eq 1 ]; then
        wait_for_tor
    else
        start_tor || return 1
    fi

    echo -e "\n  ${DIM}Connecting to ${remote_onion}:${LISTEN_PORT} via Tor...${NC}"
    echo -e "  ${DIM}[Q] Cancel${NC}\n"

    mkdir -p "$AUDIO_DIR"

    # Initial dial.
    _dial_remote
    if [ "$DIAL_RESULT" != "ok" ]; then
        case "$DIAL_RESULT" in
            cancelled) log_info "Connection cancelled." ;;
            timeout)
                log_err "Connection timed out after 120s."
                log_err "Is the remote machine running and listening? Is Tor fully bootstrapped?"
                ;;
            refused)
                log_err "Failed to connect. Check the .onion address and ensure the"
                log_err "remote machine is running 'docker compose run --rm partyline' and listening."
                ;;
        esac
        cleanup_call
        return 1
    fi

    # Connected — run the call. On a detected drop ("lost") re-dial up to
    # RECONNECT_ATTEMPTS times to ride out Tor circuit hiccups; a deliberate
    # hangup (local [Q] or remote HANGUP) ends cleanly to the menu.
    local reconnects=0
    while true; do
        touch "$CONNECTED_FLAG"
        overwrite_rm "$DROP_REASON_FILE"
        in_call_session "$RECV_PIPE" "$SEND_PIPE" "$remote_onion"

        # Why did the session end? Missing marker = unexpected death → treat as lost.
        local reason="lost"
        [ -f "$DROP_REASON_FILE" ] && reason=$(cat "$DROP_REASON_FILE" 2>/dev/null)

        # Tear down this call's transport (kills socat_call, recv_loop; removes pipes;
        # releases the wakelock). The next attempt re-acquires everything cleanly.
        cleanup_call

        if [ "$reason" = "hangup" ]; then
            break
        fi

        # Connection dropped — try to re-establish, counting each dial as one attempt.
        local reconnect_state="exhausted"
        while [ "$reconnects" -lt "$RECONNECT_ATTEMPTS" ]; do
            reconnects=$((reconnects + 1))
            echo -e "\n  ${YELLOW}${BOLD}Reconnecting (${reconnects}/${RECONNECT_ATTEMPTS})...${NC}"
            echo -e "  ${DIM}[Q] Cancel${NC}\n"
            if [ $DOCKER_MODE -eq 0 ] && ! start_tor; then
                log_err "Tor unavailable — cannot reconnect."
                reconnect_state="exhausted"
                break
            fi
            _dial_remote
            case "$DIAL_RESULT" in
                ok)        reconnect_state="ok"; break ;;
                cancelled) cleanup_call; reconnect_state="cancelled"; break ;;
                *)         cleanup_call; echo -e "  ${DIM}Re-dial failed, retrying...${NC}" ;;
            esac
        done

        case "$reconnect_state" in
            ok)        continue ;;  # socat live + pipes ready → re-enter in_call_session
            cancelled) log_info "Reconnect cancelled."; break ;;
            *)
                if [ "$RECONNECT_ATTEMPTS" -gt 0 ]; then
                    log_err "Connection lost — gave up after ${RECONNECT_ATTEMPTS} reconnect attempt(s)."
                else
                    log_err "Connection lost."
                fi
                break
                ;;
        esac
    done

    overwrite_rm "$DROP_REASON_FILE"
}

