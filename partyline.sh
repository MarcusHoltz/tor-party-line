#=============================================================================
# BOOLEAN TOGGLE SETTINGS (shared engine)
#=============================================================================

# Generic on/off settings sub-menu shared by the boolean toggles. Renders the
# standard frame for a 0/1 config variable and applies 1=on / 2=off / 0=back,
# persisting via save_config and logging. Args:
#   $1 var         name of the global 0/1 config variable
#   $2 title       banner title
#   $3 noun        subject for the "<noun> is already enabled/disabled" notice
#   $4 on_log      log_ok text after turning on
#   $5 off_log     log_ok text after turning off
#   $6 body_fn     function that prints the description block (ends with a blank line)
#   $7 sleep_secs  pause after a change
#   $8 label_color color for the "enabled" status label (e.g. $GREEN, $YELLOW)
#   $9 after_fn    optional fn run after a successful change; receives 1 (on) or 0 (off)
toggle_setting_menu() {
    local var="$1" title="$2" noun="$3" on_log="$4" off_log="$5"
    local body_fn="$6" sleep_secs="$7" label_color="$8" after_fn="${9:-}"
    local _choice
    while true; do
        clear
        header "$title"

        echo -e "  ${DIM}Status:${NC} $(onoff_label "${!var}" "$label_color")"
        echo ""

        "$body_fn"

        echo -e "  ${BOLD}${WHITE}1${NC} ${CYAN}│${NC} Turn on"
        echo -e "  ${BOLD}${WHITE}2${NC} ${CYAN}│${NC} Turn off"
        echo -e "  ${BOLD}${WHITE}0${NC} ${CYAN}│${NC} ${DIM}Back${NC}"
        echo ""
        echo -ne "  ${BOLD}Select: ${NC}"
        read -r _choice

        case "$_choice" in
            1)
                if [ "${!var}" -eq 1 ]; then
                    log_info "$noun is already enabled"
                else
                    printf -v "$var" 1
                    save_config
                    log_ok "$on_log"
                    [ -n "$after_fn" ] && "$after_fn" 1
                fi
                sleep "$sleep_secs"
                ;;
            2)
                if [ "${!var}" -eq 0 ]; then
                    log_info "$noun is already disabled"
                else
                    printf -v "$var" 0
                    save_config
                    log_ok "$off_log"
                    [ -n "$after_fn" ] && "$after_fn" 0
                fi
                sleep "$sleep_secs"
                ;;
            0|q|Q) return ;;
            *)
                menu_invalid
                ;;
        esac
    done
}

