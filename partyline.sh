# AUTO-LISTEN (BACKGROUND LISTENER)
#=============================================================================

start_auto_listener() {
    # Only start if auto-listen is enabled and Tor is running
    if [ "$AUTO_LISTEN" -ne 1 ]; then return 0; fi
    if [ -z "$SHARED_SECRET" ]; then return 0; fi
    # Docker mode: Tor managed by entrypoint.sh, TOR_PID is empty but Tor IS running
    # Script mode: check TOR_PID
    if [ $DOCKER_MODE -eq 0 ]; then
        if [ -z "$TOR_PID" ] || ! kill -0 "$TOR_PID" 2>/dev/null; then return 0; fi
    else
        # Verify Tor is actually up by checking the hidden service hostname file
        if [ ! -f "$ONION_FILE" ]; then return 0; fi
    fi

    # Stop any existing listener first
    stop_auto_listener

    mkdir -p "$AUDIO_DIR" "$DATA_DIR/run"
    overwrite_rm "$RECV_PIPE" "$SEND_PIPE" "$AUTO_LISTEN_FLAG"
    mkfifo "$RECV_PIPE" "$SEND_PIPE"

    socat "TCP-LISTEN:$LISTEN_PORT,reuseaddr" \
        "SYSTEM:touch $AUTO_LISTEN_FLAG; cat $SEND_PIPE & cat > $RECV_PIPE" \
        2>/dev/null &
    AUTO_LISTEN_PID=$!
    save_pid "socat" "$AUTO_LISTEN_PID"
}

stop_auto_listener() {
    kill_pid "$AUTO_LISTEN_PID"
    AUTO_LISTEN_PID=""
    overwrite_rm "$AUTO_LISTEN_FLAG" "$RECV_PIPE" "$SEND_PIPE"
}

