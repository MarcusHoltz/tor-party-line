# Query Tor control port for active circuit hops
# Run one Tor control-port session: AUTHENTICATE, the GETINFO command(s) piped
# in on stdin, then QUIT — emitting the response with CRs stripped. Reads
# $cookie_hex from the calling scope (get_circuit_hops).
tor_ctrl() {
    {
        printf 'AUTHENTICATE %s\r\n' "$cookie_hex"
        cat
        printf 'QUIT\r\n'
    } | socat - "TCP:127.0.0.1:$TOR_CONTROL_PORT" 2>/dev/null | tr -d '\r'
}

# Outputs one line per hop: "relay_name|country_name"
# Returns 1 if circuit info is unavailable
get_circuit_hops() {
    [ "$SHOW_CIRCUIT" -eq 0 ] && return 1

    # Cookie path differs between Docker mode and script mode:
    # - Docker: DataDirectory=/var/lib/tor → cookie at /var/lib/tor/control_auth_cookie
    # - Script mode: DataDirectory=$TOR_DIR/data → cookie at $TOR_DIR/data/control_auth_cookie
    local cookie_file
    if [ $DOCKER_MODE -eq 1 ]; then
        cookie_file="/var/lib/tor/control_auth_cookie"
    else
        cookie_file="$TOR_DIR/data/control_auth_cookie"
    fi
    [ ! -f "$cookie_file" ] && return 1

    local cookie_hex
    cookie_hex=$(od -An -tx1 "$cookie_file" | tr -d ' \n' 2>/dev/null) || return 1
    [ -z "$cookie_hex" ] && return 1

    # Step 1: Get circuit status
    local circ_resp
    circ_resp=$(printf 'GETINFO circuit-status\r\n' | tor_ctrl) || return 1

    echo "$circ_resp" | grep -q "^250 OK" || return 1

    # Find best BUILT circuit — prefer HS circuits
    local circuit_line
    circuit_line=$(echo "$circ_resp" | grep " BUILT " \
        | grep -E "PURPOSE=HS_SERVICE_INTRO|PURPOSE=HS_CLIENT_REND" | head -1) || true
    [ -z "$circuit_line" ] && circuit_line=$(echo "$circ_resp" | grep " BUILT " | head -1)
    [ -z "$circuit_line" ] && return 1

    # Extract path (field 3: comma-separated $FP~Name entries)
    local path
    path=$(echo "$circuit_line" | awk '{print $3}')
    [ -z "$path" ] && return 1

    # Parse relay names and fingerprints
    local names=() fps=()
    IFS=',' read -ra relays <<< "$path"
    for r in "${relays[@]}"; do
        local name fp
        if [[ "$r" == *"~"* ]]; then
            name="${r#*~}"; fp="${r%%~*}"
        else
            name="${r:0:8}..."; fp="$r"
        fi
        names+=("$name")
        fps+=("${fp#\$}")
    done
    [ ${#names[@]} -eq 0 ] && return 1

    # Three separate socat sessions (circuit → ns/id → ip-to-country) rather than one:
    # GETINFO circuit-status returns a variable-length multi-line body that a single
    # persistent socat connection would need to know when to stop reading. Separate
    # sessions avoid parsing interleaved command/response boundaries.

    # Step 2: Get IPs for all relays via ns/id (single session)
    local ns_resp
    ns_resp=$({
        for fp in "${fps[@]}"; do
            printf 'GETINFO ns/id/%s\r\n' "$fp"
        done
    } | tor_ctrl) || true

    local ips=()
    if [ -n "$ns_resp" ]; then
        while IFS= read -r rline; do
            ips+=("$(echo "$rline" | awk '{print $7}')")
        done <<< "$(echo "$ns_resp" | grep '^r ')"
    fi

    # Step 3: Resolve countries for all IPs (single session)
    local countries=()
    local has_ips=0
    for ip in "${ips[@]}"; do [ -n "$ip" ] && has_ips=1 && break; done

    if [ "$has_ips" -eq 1 ]; then
        local cc_resp
        cc_resp=$({
            for ip in "${ips[@]}"; do
                [ -n "$ip" ] && printf 'GETINFO ip-to-country/%s\r\n' "$ip"
            done
        } | tor_ctrl) || true

        if [ -n "$cc_resp" ]; then
            while IFS= read -r ccline; do
                local cc
                cc=$(echo "$ccline" | sed 's/.*=//')
                countries+=("$(cc_to_country "$cc")")
            done <<< "$(echo "$cc_resp" | grep 'ip-to-country')"
        fi
    fi

    # Output one line per hop: "name|country"
    local total=${#names[@]}
    for ((i = 0; i < total; i++)); do
        local country="${countries[$i]:-??}"
        echo "${names[$i]}|${country}"
    done
    return 0
}

